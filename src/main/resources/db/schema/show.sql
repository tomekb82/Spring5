CREATE TABLE SHOW (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1),
  date DATE,
  movie_id BIGINT,
  room_id BIGINT,

  PRIMARY KEY (id),

  CONSTRAINT FKK_MOVIE FOREIGN KEY (movie_id) REFERENCES MOVIE(id),
  CONSTRAINT FKK_ROOM FOREIGN KEY (room_id) REFERENCES ROOM(id)
);

